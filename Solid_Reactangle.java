import java.util.*;
public class Solid_Reactangle {
    public static void main(String[] args) {
        // For Input
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();
        // Outer Loop
        for(int i=1;i<=n;i++) {
            // Inner Loop
            for(int j=1;j<=m;j++) {
                System.out.print("*");
            } System.out.println();
        } 
        }
    }
