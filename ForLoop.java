public class ForLoop {
    public static void main(String[] args) {
        // for(initialisation;condition;pdation)
        // Counter ++ => Counter = Counter + 1 
        for(int counter = 0;counter <101; counter = counter + 1) {
            System.out.println("Hello World ");
            // We can use 'i' instead of Counter.
        }
    }
}
